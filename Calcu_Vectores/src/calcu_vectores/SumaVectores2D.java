    private void btnGraficarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGraficarActionPerformed
        Grafica2D graf = new Grafica2D();
        Double valores[][]=tomarValores(m).clone();
        Double nuevoX;
        Double nuevoY;
        for(int i = 0; i<valores[0].length;i++){
            for(int z=0; z<valores.length;z++){
                if(z==0){//Para el primer vector
                    nuevoX= valores[z][0];
                    nuevoY= valores[z][1];
                }else{ //Para los vectores siguientes
                    nuevoX= valores[z][0]+valores[z-1][0];
                    nuevoY= valores[z][1]+valores[z-1][1];
                }
                
                double arrayX[] = agregarValor(x,nuevoX);
                double arrayY[] = agregarValor(y,nuevoY);
                x= null;
                y=null;
                x=arrayX.clone();
                y=arrayY.clone();
                //Graficar cada vector
                graf.plot.removeAllPlots();
                graf.plot.addLinePlot("Vectores",Color.BLUE,arrayX,arrayY);
                //graf.espaGrafica.setVisible(true);
            }
        }
        //Para graficar vector Resultante
        grafX= result[0][0];
        grafY = result[0][1];
        arrX= agregarValor(x1, grafX).clone();
        arrY=agregarValor(y2, grafY).clone();
        graf.plot.addLinePlot("Vector Resultante",Color.RED,arrX,arrY);
        
    }//GEN-LAST:event_btnGraficarActionPerformed

    public Double[][] tomarValores(JTextField mOperaciones[][]) {
 
        Double mRetornada[][] = new Double[mOperaciones.length][mOperaciones[0].length];
 
        for (int i = 0; i < mOperaciones.length; i++) {
            for (int j = 0; j < mOperaciones[0].length; j++) {
                mRetornada[i][j] = Double.parseDouble(mOperaciones[i][j].getText());
            }
        }
 
        return mRetornada;
    }
    
    //Agregar valores de matriz para graficar
    public double[] agregarValor(double[]arr, double valor){
        double []newA = new double[arr.length+1];
        
        for(int i=0; i<arr.length;i++){
            newA[i]=arr[i];
        }
        newA[newA.length-1] = valor;
        
        return newA;
    }
    
