    private void btnAgregarRActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarRActionPerformed
        if(cboxFilaR.getSelectedItem().equals("0")){
            JOptionPane.showMessageDialog(rootPane, "SELECCIONA VALORES PARA EL TAMAÃ‘O DE LA MATRIZ");
        }else{
            int setX = 20, setY = 20;
            filas=Integer.parseInt((String) cboxFilaR.getSelectedItem());
            colum = 2;
            m=new JTextField[filas][colum];

            for (int i = 0; i < filas; i++) {
                for (int j = 0; j < colum; j++) {
                    m[i][j] = new JTextField(" ");
                    m[i][j].setBounds(setX, setY, 30, 30);
                    m[i][j].setHorizontalAlignment(JTextField.RIGHT);
                    setX += 35;
                }
                setX = 20;
                setY += 35;
            }

            for (int z = 0; z < filas; z++) {
                for (int y = 0; y < colum; y++) {
                    jPaneR.add(m[z][y]);
                    m[z][y].setVisible(false);
                }
            }
            for (int l = 0; l < filas; l++) {
                for (int j = 0; j < colum; j++) {
                    m[l][j].setVisible(true);
                }
            
            }}
    }//GEN-LAST:event_btnAgregarRActionPerformed

