    private void btnGraficarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGraficarActionPerformed
        //Establecer puntos para graficar
        grafX1= Double.parseDouble(txtComponenteA1.getText());
        grafX2= Double.parseDouble(txtComponenteA2.getText());
        grafY1= Double.parseDouble(txtComponenteB1.getText());
        grafY2= Double.parseDouble(txtComponenteB2.getText());
        grafZ1= Double.parseDouble(txtComponenteC1.getText());
        grafZ2= Double.parseDouble(txtComponenteC2.getText());
        grafRX= Double.parseDouble(txtResultA.getText());
        grafRY= Double.parseDouble(txtResultB.getText());
        grafRZ= Double.parseDouble(txtResultC.getText());
        //Crear arreglos a graficar
        arrX1= hacerArreglo(x, grafX1);
        arrY1=hacerArreglo(y, grafY1);
        arrZ1=hacerArreglo(z, grafZ1);
        arrX2= hacerArreglo(x, grafX2);
        arrY2=hacerArreglo(y, grafY2);
        arrZ2=hacerArreglo(z, grafZ2);
        arrRX=hacerArreglo(x,grafRX);
        arrRY=hacerArreglo(y,grafRY);
        arrRZ=hacerArreglo(z,grafRZ);
        //GRAFICAR
        Grafica3D graf = new Grafica3D();
        graf.grafica3D.addLinePlot("Vector 1",Color.BLUE,arrX1,arrY1,arrZ1);
        graf.grafica3D.addLinePlot("Vector 2",Color.RED,arrX2,arrY2,arrZ2);
        graf.grafica3D.addLinePlot("Vector Producto Cruz",Color.MAGENTA,arrRX,arrRY,arrRZ);
        graf.espaGrafica.setVisible(true);
    }//GEN-LAST:event_btnGraficarActionPerformed

    public double[] hacerArreglo(double[]arr, double valor){
        double []newA = new double[arr.length];
        
        for(int i=0; i<arr.length;i++){
            newA[i]=arr[i];
        }
        newA[newA.length-1] = valor;
        
        return newA;
    }
